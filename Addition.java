class Addition
	{
		public static void main(String[] args)
			{
				byte a=10;
				byte b=20;
				int c=a+b;
				System.out.println(c);// addition of two variable of byte data type will give int data type as result
				short d=2000;
				short e=3000;
				int f=d+e;// addition of two variable of short data type will give int data type as result
				System.out.println(f);
				int g=5000;	
				int h=4000;
				int i=g+h;// addition of two variable of int data type will give int data type as result
				System.out.println(i);
				char j='a';//ascii code=97
				char k='b';//ascii code=98
				int l=j+k;//97+98=195.value will be stored as 195
				System.out.println(l);
				long m=50l;
				long n=40l;
				long  o=m+n;
				System.out.println(o);//addition of two variable of long data type will give long data type as result
				float p=1.1f;
				float q=2.2f;
				float r=p+q;//addition of two variable of float data type will give float data type as result
				System.out.println(r);
				double s=1.1;
				double t=2.2;
				double u=s+t;
				System.out.println(u);//addition of two variable of double data type will give double data type as result
				String v="hello";
				String w="hi";
				String x=v+w;
				System.out.println(x);

			}
	}