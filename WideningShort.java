class WideningShort
	{
		public static void main(String[] args)
			{
			   short a=100;
			  
			   int  c=a;
			   long d=a;
			  
			  float e=a;
			  double f=a;
			   
			
			System.out.println(c);
			System.out.println(d);

			System.out.println(e);
			System.out.println(f);
			

			

			
			}
	}

